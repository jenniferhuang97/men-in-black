    private void DisplayTable(){
    
        try
        {
           con = DriverManager.getConnection("jdbc:mysql://mysqlse.fragnet.net:3306/111653_clientdb", "111653" ,"81374364");
           String sql = "select `användarnamn`, `möte`, `möteID` from `user_skapat_möte` where `användarnamn`=?";
           PreparedStatement pstmt = con.prepareStatement(sql);
           pstmt.setString(1, inloggadPerson);
           ResultSet rs = pstmt.executeQuery();
           tblMöten.setModel(DbUtils.resultSetToTableModel(rs));
           tblMöten.getTableHeader();
           tblMöten.getColumnModel().getColumn(0).setHeaderValue("Användarnamn");
           tblMöten.getColumnModel().getColumn(1).setHeaderValue("Möte");
           tblMöten.getColumnModel().getColumn(2).setHeaderValue("Mötes-ID");
           tblMöten.getTableHeader().repaint();
           
       }
       
       catch (Exception ex){
       
       }
   }
    
    public void hämtaMöteVärde(){
    int rad = tblMöten.getSelectedRow();                                   //Visar markrad rad i tabellen
    möteVärde = tblMöten.getModel().getValueAt(rad, 2).toString();
    }
    
