    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        LocalDateTime bekräftadTid = (LocalDateTime)tidFörslagsCombobox.getSelectedItem();
        MötesFörslag förslag = (MötesFörslag)mötesCombobox.getSelectedItem();
        
        boolean lyckad = meetingRepository.bekräftaMöteFörAnvändare(förslag.getMötesFk(), userId, bekräftadTid);
        if(lyckad) {
            JOptionPane.showMessageDialog(null, "Du har bekräftat tiden: " + bekräftadTid + " för mötet: " + förslag.getMötesTitel());
        } else {
            JOptionPane.showMessageDialog(null, "Misslyckat");
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

