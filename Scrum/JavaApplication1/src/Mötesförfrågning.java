    public Mötesförfrågning(String anvandarnamn) {
        initComponents();
        userRepository = new UserRepository();
        meetingRepository = new MeetingRepository();
        inloggadPerson = anvandarnamn;
        getUserId(anvandarnamn);
        hämtaMötesFörslag(userId);
        läggTillMötesFörslagTillCombobox();
    }
    
    private void getUserId(String anvandarnamn) {
         userId = userRepository.getUserIdForUsernamn(anvandarnamn);
    }
    
    private void hämtaMötesFörslag(Integer userId) {
        mötesFörslagFörAnvändare = meetingRepository.getMeetingsForUser(userId);
    }
    
    private void läggTillMötesFörslagTillCombobox() {
        for(MötesFörslag förslag : mötesFörslagFörAnvändare) {
            mötesCombobox.addItem(förslag);
        }
    }
    
    private void läggTillFörslagsTider(MötesFörslag förslag) {
        tidFörslagsCombobox.removeAllItems();
        tidFörslagsCombobox.addItem(förslag.getFörslag1());
        tidFörslagsCombobox.addItem(förslag.getFörslag2());
        tidFörslagsCombobox.addItem(förslag.getFörslag3());
    }
    

